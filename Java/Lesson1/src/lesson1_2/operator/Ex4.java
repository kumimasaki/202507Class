package lesson1_2.operator;

public class Ex4 {

	public static void main(String[] args) {
		// 関係演算子
		// 変数の初期化
		int a = 10;
		int b = 20;
		
		// aがbより小さい場合（<）
		System.out.println(a < b);
		// aがbより大きい場合(>)
		System.out.println(a > b);
		// aとbが等しい（==）
		System.out.println(a == b);
		// aとbが等しくない（!=）
		System.out.println(a != b);
	}

}
